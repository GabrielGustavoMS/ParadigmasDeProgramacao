#include <stdio.h>

int main(void) {
	printf("%d\n",sizeof(int));//Imprime tamanho de um inteiro
	printf("%d\n",sizeof(char));//Imprime tamanho de um char
	printf("%d\n",sizeof(float));//Imprime tamanho de um float
	printf("%d\n",sizeof(double));//Imprime tamanho de um double
	printf("%d\n",sizeof(int*));//Imprime tamanho de um ponteiro de inteiro
	return 0;
}
